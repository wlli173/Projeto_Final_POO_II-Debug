    public void carregarUsuariosNaLista() {
        
        UsuariosDAO usuarioDAO = new UsuariosDAO();
        List<Usuario> usuarios = usuarioDAO.listarUsuarios();

        DefaultListModel<String> modelo = (DefaultListModel<String>) listaUsuarios.getModel();
        modelo.clear(); // Limpa a lista antes de adicionar os dados

        for (Usuario u : usuarios) {
            modelo.addElement(u.getNome()); // Adiciona apenas o nome do usuário
        }
    }


