    private void btnCriarProjetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarProjetoActionPerformed
        
        //TODO adicionar verificação dos campos
        
        ProjetoDAO projetoDAO = new ProjetoDAO();
        MembrosProjetoDAO membrosProjetoDAO = new MembrosProjetoDAO();
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String dataAtual = LocalDate.now().format(formatter);

        Projeto projeto = new Projeto(
            txtfNomeProjeto.getText(),
            txtfDescricaoProjeto.getText(),
            dataAtual,
            formatedTxtfCampoDataConclusao.getText(),
            SessaoUsuario.getUsuarioLogado().getIdUsuario()
        );
        
        int idProjetoGerado = projetoDAO.inserirProjetoRetornandoId(projeto);
        
        List<String> usuariosSelecionados = listaUsuarios.getSelectedValuesList();
        
        UsuariosDAO usuariosDAO = new UsuariosDAO();

        for(String nomeUsuario: usuariosSelecionados){
            
            membrosProjetoDAO.inserirMembro(idProjetoGerado, usuariosDAO.buscarUsuarioPorNome(nomeUsuario).getIdUsuario());
            
        }
        
        JOptionPane.showMessageDialog(null, "Prjeto Criado com sucesso");
        
        mediator.getTelaPrincipal().mostrarPainel("Projetos");
        
    }//GEN-LAST:event_btnCriarProjetoActionPerformed

